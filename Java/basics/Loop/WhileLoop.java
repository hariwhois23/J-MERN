
public class WhileLoop {
    public static void main(String[] args) {
        // WHile loop
        int num = 1;
        while (num <= 5) {
            System.out.println(num);
            num++;
        }

        // Do while loop
        int i = 1;
        do {
            System.out.println(i);
            i++;
        } while (i == 1);
    }
}
